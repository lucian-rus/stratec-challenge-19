/*
 * simple class that alters the look and feel of the array
 * inspired by Devon Crawford's style class in his A*Pathfinding Application
 */

import java.awt.*;

public @interface style {
    Color greenHighlight = new Color(150, 255, 152);
    Color yellowHighlight = new Color(255, 255, 136);
    Color blueHighlight = new Color(130, 216, 230);
    Color redHighlight = new Color(175, 60, 60);
}